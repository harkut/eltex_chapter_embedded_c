/*
  strChar(const char* str, int c)
  Описание:
  Получает аргументом строку str и ищет в ней первое вхождение символа s.
  Возвращает указатель на первое вхождение символа s.
*/
char* strChar(const char* str, int s) {
  int i = 0;
  
  while (str[i] && str[i] != s) {
    ++i;
  }
  return s == str[i] ? (char*)str + i : NULL;
}
